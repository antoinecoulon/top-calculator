function add(n1, n2) {
    let result = n1 + n2;
    return result;
}

function substract(n1, n2) {
    let result = n1 - n2;
    return result;
}

function multiply(n1, n2) {
    let result = n1 * n2;
    return result;
}

function divide(n1, n2) {
    let result = n1 / n2;
    return result;
}

